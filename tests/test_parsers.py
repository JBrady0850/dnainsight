_ANCESTRYDNA_CONTENT = """\
# AncestryDNA raw data download
# This file was generated by AncestryDNA at: Wed Jun 24 12:00:00 2026
#
# rsid\tchromosome\tposition\tallele1\tallele2
rs1801133\t1\t11856378\tA\tA
rs4680\t22\t19951271\tA\tG
rs671\t12\t111803962\tG\tG
rs1051730\t15\t78806023\tT\tT
rs00000_invalid\t1\t999\tN\tN
"""

_23ANDME_CONTENT = """\
# This data file generated by 23andMe at: Wed Jun 24 12:00:00 2026
#
# rsid\tchromosome\tposition\tgenotype
rs1801133\t1\t11856378\tAA
rs4680\t22\t19951271\tAG
rs671\t12\t111803962\tGG
rs1051730\t15\t78806023\tTT
"""


def _write_tmp(tmp_path, filename, content):
    p = tmp_path / filename
    p.write_text(content, encoding="utf-8")
    return str(p)


# ---------------------------------------------------------------------------
# parse_dna_file — AncestryDNA
# parse_dna_file returns: {'provider': str, 'snp_count': int, 'snps': list[dict]}
# ---------------------------------------------------------------------------

class TestParseAncestryDNA:
    def test_returns_dict_with_snps_key(self, tmp_path):
        path = _write_tmp(tmp_path, "ancestry.txt", _ANCESTRYDNA_CONTENT)
        result = parse_dna_file(path)
        assert isinstance(result, dict)
        assert "snps" in result
        assert isinstance(result["snps"], list)

    def test_provider_detected(self, tmp_path):
        path = _write_tmp(tmp_path, "ancestry.txt", _ANCESTRYDNA_CONTENT)
        result = parse_dna_file(path)
        assert result.get("provider") == "ancestrydna"

    def test_snps_non_empty(self, tmp_path):
        path = _write_tmp(tmp_path, "ancestry.txt", _ANCESTRYDNA_CONTENT)
        result = parse_dna_file(path)
        assert len(result["snps"]) > 0

    def test_rsid_in_snps(self, tmp_path):
        path = _write_tmp(tmp_path, "ancestry.txt", _ANCESTRYDNA_CONTENT)
        result = parse_dna_file(path)
        rsids = [r["rsid"] for r in result["snps"]]
        assert "rs4680" in rsids

    def test_alleles_present(self, tmp_path):
        path = _write_tmp(tmp_path, "ancestry.txt", _ANCESTRYDNA_CONTENT)
        result = parse_dna_file(path)
        row = next(r for r in result["snps"] if r["rsid"] == "rs4680")
        assert row["allele1"] == "A"
        assert row["allele2"] == "G"

    def test_chromosome_and_position(self, tmp_path):
        path = _write_tmp(tmp_path, "ancestry.txt", _ANCESTRYDNA_CONTENT)
        result = parse_dna_file(path)
        row = next(r for r in result["snps"] if r["rsid"] == "rs4680")
        assert row["chromosome"] == "22"
        assert str(row["position"]) == "19951271"

    def test_snp_count_matches_snps_length(self, tmp_path):
        path = _write_tmp(tmp_path, "ancestry.txt", _ANCESTRYDNA_CONTENT)
        result = parse_dna_file(path)
        assert result["snp_count"] == len(result["snps"])


