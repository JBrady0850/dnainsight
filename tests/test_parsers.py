class TestDetectProvider:
    def test_ancestrydna_header(self):
        headers = ["# AncestryDNA raw data"]
        col    = "rsid\tchromosome\tposition\tallele1\tallele2"
        assert detect_provider(headers, col) == "ancestrydna"

    def test_23andme_header(self):
        headers = ["# This data file generated by 23andMe"]
        col    = "# rsid\tchromosome\tposition\tgenotype"
        assert detect_provider(headers, col) == "23andme"

    def test_myheritage_header(self):
        headers = ["# MyHeritage DNA raw data"]
        col    = "rsid\tchromosome\tposition\tgenotype"
        assert detect_provider(headers, col) == "myheritage"

    def test_ftdna_header(self):
        headers = ["# FamilyTreeDNA raw data export"]
        col    = "RSID\tCHROMOSOME\tPOSITION\tRESULT"
        assert detect_provider(headers, col) == "ftdna"

    def test_livingdna_header(self):
        headers = ["# LivingDNA raw data"]
        col    = "rsid\tchromosome\tposition\tgenotype"
        assert detect_provider(headers, col) == "livingdna"

    def test_column_fallback_ancestrydna(self):
        """No provider header; detect from allele1/allele2 columns."""
        assert detect_provider([], "rsid\tchromosome\tposition\tallele1\tallele2") == "ancestrydna"

    def test_column_fallback_23andme(self):
        assert detect_provider([], "rsid\tchromosome\tposition\tgenotype") == "23andme"

    def test_column_fallback_ftdna(self):
        assert detect_provider([], "RSID\tCHROMOSOME\tPOSITION\tRESULT") == "ftdna"

    def test_generic_fallback(self):
        assert detect_provider([], "id\tchr\tpos\tdata") == "generic"


